package com.alarm.vo;

public class AlarmInfo {
	private String alarmId;
	private String alarmTime;
	private String alarmType;
	private String alarmLocation;
	private String alarm_lat;
	private String alarm_lng;
		
	public String getAlarmId() {
		return alarmId;
	}
	public void setAlarmId(String alarmId) {
		this.alarmId = alarmId;
	}
	public String getAlarmTime() {
		return alarmTime;
	}
	public void setAlarmTime(String alarmTime) {
		this.alarmTime = alarmTime;
	}
	public String getAlarmType() {
		return alarmType;
	}
	public void setAlarmType(String alarmType) {
		this.alarmType = alarmType;
	}
	public String getAlarmLocation() {
		return alarmLocation;
	}
	public void setAlarmLocation(String alarmLocation) {
		this.alarmLocation = alarmLocation;
	}
	public String getAlarm_lat() {
		return alarm_lat;
	}
	public void setAlarm_lat(String alarm_lat) {
		this.alarm_lat = alarm_lat;
	}
	public String getAlarm_lng() {
		return alarm_lng;
	}
	public void setAlarm_lng(String alarm_lng) {
		this.alarm_lng = alarm_lng;
	}
}
